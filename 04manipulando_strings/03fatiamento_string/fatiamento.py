nome = "Emanuel de Jesus Nascimento"

print(nome[0]) # Primeiro caractere do nome

print(nome[:7]) # Os 7 primeiros caracteres do nome

print(nome[10:]) # Caracteres a partir da posição 10 até o final

print(nome[10:16])  # Caracteres a partir da posição  10 até, mas não incluindo, a posição 16.

print(nome[10:16:2]) # Pula um caractere entre as letras (de 10 em  2)

print(nome[:])  # Todo o nome

print(nome[::-1]) # O nome invertido